"""
GPTCloneBench Evaluation Script for Type-IV Code Clone Detector.

This script evaluates the Type-IV clone detector specifically on the GPTCloneBench
dataset, which contains AI-generated code clones with semantic relationships.

GPTCloneBench Dataset:
- Contains code pairs generated by GPT models
- Includes semantic clone labels (T1-T4 types)
- Focuses on Type-IV (semantic) clones
- Metadata includes: language, prompt, filename, clone type

Usage:
    # Evaluate on full GPTCloneBench dataset
    poetry run python evaluate_gptclonebench.py \
        --model models/type4_xgb.pkl \
        --dataset ../../../../datasets/gptclonebench/gptclonebench_dataset.jsonl

    # Evaluate with visualizations
    poetry run python evaluate_gptclonebench.py \
        --model models/type4_xgb.pkl \
        --dataset ../../../../datasets/gptclonebench/gptclonebench_dataset.jsonl \
        --visualize \
        --output-dir ./gptclonebench_results

    # Evaluate with sampling
    poetry run python evaluate_gptclonebench.py \
        --model models/type4_xgb.pkl \
        --dataset ../../../../datasets/gptclonebench/gptclonebench_dataset.jsonl \
        --sample-size 500 \
        --visualize
"""

import argparse
import json
import logging
from pathlib import Path
from typing import Optional

import numpy as np
import pandas as pd
from sklearn.metrics import (
    accuracy_score,
    classification_report,
    confusion_matrix,
    f1_score,
    precision_score,
    recall_score,
    roc_auc_score,
)
from tqdm import tqdm

from clone_detection.features.sheneamer_features import SheneamerFeatureExtractor
from clone_detection.models.classifiers import SemanticClassifier
from clone_detection.utils.common_setup import setup_logging
from clone_detection.utils.metrics_visualization import MetricsVisualizer

logger = setup_logging(__name__)


def load_gptclonebench_dataset(
    dataset_path: str,
    sample_size: Optional[int] = None,
    clone_type_filter: Optional[str] = None,
) -> tuple[list[str], list[str], list[int], list[dict]]:
    """
    Load GPTCloneBench dataset with detailed metadata.

    Args:
        dataset_path: Path to JSONL file
        sample_size: Optional sample size
        clone_type_filter: Optional filter by clone type (e.g., 'T4')

    Returns:
        Tuple of (code1_list, code2_list, labels, metadata_list)
    """
    code1_list = []
    code2_list = []
    labels = []
    metadata_list = []

    logger.info(f"Loading GPTCloneBench dataset from {dataset_path}...")

    with open(dataset_path, "r", encoding="utf-8") as f:
        total_lines = sum(1 for _ in f)

    logger.info(f"Found {total_lines} entries in GPTCloneBench")

    with open(dataset_path, "r", encoding="utf-8") as f:
        for i, line in enumerate(f):
            if sample_size and i >= sample_size:
                break

            try:
                data = json.loads(line)
                code1 = data.get("code1", "")
                code2 = data.get("code2", "")
                label = 1 if data.get("semantic", False) else 0
                metadata = data.get("metadata", {})

                # Filter by clone type if specified
                if clone_type_filter:
                    if metadata.get("type") != clone_type_filter:
                        continue

                if code1 and code2:
                    code1_list.append(code1)
                    code2_list.append(code2)
                    labels.append(label)
                    metadata_list.append(metadata)

            except json.JSONDecodeError as e:
                logger.warning(f"Failed to parse line {i}: {e}")

            if (i + 1) % 100 == 0:
                logger.info(f"  Processed {i + 1} entries")

    logger.info(f"Loaded {len(code1_list)} code pairs")

    # Log dataset statistics
    if metadata_list:
        languages = [m.get("language", "unknown") for m in metadata_list]
        clone_types = [m.get("type", "unknown") for m in metadata_list]
        prompts = [m.get("prompt", "unknown") for m in metadata_list]

        logger.info(f"Languages: {pd.Series(languages).value_counts().to_dict()}")
        logger.info(f"Clone Types: {pd.Series(clone_types).value_counts().to_dict()}")
        logger.info(f"Prompts: {pd.Series(prompts).value_counts().to_dict()}")

    return code1_list, code2_list, labels, metadata_list


def analyze_by_prompt(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    y_scores: np.ndarray,
    metadata: list[dict],
) -> pd.DataFrame:
    """
    Analyze performance by prompt category.

    Args:
        y_true: True labels
        y_pred: Predicted labels
        y_scores: Prediction scores
        metadata: List of metadata dictionaries

    Returns:
        DataFrame with performance metrics per prompt
    """
    prompts = [m.get("prompt", "unknown") for m in metadata]

    results = []
    for prompt in set(prompts):
        mask = [p == prompt for p in prompts]
        if sum(mask) < 5:  # Skip small groups
            continue

        y_true_p = y_true[mask]
        y_pred_p = y_pred[mask]
        y_scores_p = y_scores[mask]

        if len(y_true_p) == 0:
            continue

        results.append(
            {
                "prompt": prompt,
                "samples": len(y_true_p),
                "accuracy": accuracy_score(y_true_p, y_pred_p),
                "precision": precision_score(y_true_p, y_pred_p, zero_division=0),
                "recall": recall_score(y_true_p, y_pred_p, zero_division=0),
                "f1": f1_score(y_true_p, y_pred_p, zero_division=0),
                "roc_auc": roc_auc_score(y_true_p, y_scores_p)
                if len(set(y_true_p)) > 1
                else 0.5,
            }
        )

    return pd.DataFrame(results).sort_values("samples", ascending=False)


def analyze_by_clone_type(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    y_scores: np.ndarray,
    metadata: list[dict],
) -> pd.DataFrame:
    """
    Analyze performance by clone type.

    Args:
        y_true: True labels
        y_pred: Predicted labels
        y_scores: Prediction scores
        metadata: List of metadata dictionaries

    Returns:
        DataFrame with performance metrics per clone type
    """
    clone_types = [m.get("type", "unknown") for m in metadata]

    results = []
    for ctype in set(clone_types):
        mask = [t == ctype for t in clone_types]
        if sum(mask) < 5:
            continue

        y_true_t = y_true[mask]
        y_pred_t = y_pred[mask]
        y_scores_t = y_scores[mask]

        if len(y_true_t) == 0:
            continue

        results.append(
            {
                "clone_type": ctype,
                "samples": len(y_true_t),
                "accuracy": accuracy_score(y_true_t, y_pred_t),
                "precision": precision_score(y_true_t, y_pred_t, zero_division=0),
                "recall": recall_score(y_true_t, y_pred_t, zero_division=0),
                "f1": f1_score(y_true_t, y_pred_t, zero_division=0),
                "roc_auc": roc_auc_score(y_true_t, y_scores_t)
                if len(set(y_true_t)) > 1
                else 0.5,
            }
        )

    return pd.DataFrame(results).sort_values("samples", ascending=False)


def evaluate_gptclonebench(
    model_path: str,
    dataset_path: str,
    language: str = "java",
    sample_size: Optional[int] = None,
    clone_type_filter: Optional[str] = None,
    visualize: bool = True,
    output_dir: Optional[str] = None,
    threshold: Optional[float] = None,
    threshold_sweep: bool = False,
) -> dict:
    """
    Evaluate model on GPTCloneBench dataset.

    Args:
        model_path: Path to trained model
        dataset_path: Path to GPTCloneBench JSONL file
        language: Programming language
        sample_size: Optional sample size
        clone_type_filter: Optional filter by clone type
        visualize: Whether to generate visualizations
        output_dir: Directory for output files
        threshold: Custom decision threshold (default: use model's calibrated threshold)
        threshold_sweep: Whether to perform threshold sweep analysis

    Returns:
        Evaluation metrics dictionary
    """
    logger.info("=" * 60)
    logger.info("GPTCloneBench Evaluation")
    logger.info("=" * 60)

    # Load model
    logger.info(f"Loading model from {model_path}...")
    model = SemanticClassifier.load(Path(model_path).name)

    # Set custom threshold if provided
    if threshold is not None:
        model.set_threshold(threshold)
        logger.info(f"Using custom threshold: {threshold:.3f}")
    else:
        logger.info(f"Using model's calibrated threshold: {model.get_threshold():.3f}")

    # Load dataset
    code1_list, code2_list, labels, metadata = load_gptclonebench_dataset(
        dataset_path,
        sample_size=sample_size,
        clone_type_filter=clone_type_filter,
    )

    if len(code1_list) == 0:
        raise ValueError("No valid code pairs found in dataset")

    # Extract features
    logger.info(f"Extracting features for {len(code1_list)} pairs...")
    extractor = SheneamerFeatureExtractor()
    features = []

    for code1, code2 in tqdm(
        zip(code1_list, code2_list), total=len(code1_list), desc="Extracting features"
    ):
        try:
            fused = extractor.extract_fused_features(code1, code2, language)
            features.append(fused)
        except Exception as e:
            logger.warning(f"Feature extraction failed: {e}")
            features.append(np.zeros(extractor.n_fused_features))

    X_test = np.array(features)
    y_test = np.array(labels)

    # Make predictions
    logger.info("Making predictions...")
    y_pred = model.predict(X_test)
    y_proba = model.predict_proba(X_test)[:, 1]

    # Calculate overall metrics
    metrics = {
        "accuracy": accuracy_score(y_test, y_pred),
        "precision": precision_score(y_test, y_pred, zero_division=0),
        "recall": recall_score(y_test, y_pred, zero_division=0),
        "f1": f1_score(y_test, y_pred, zero_division=0),
        "roc_auc": roc_auc_score(y_test, y_proba),
        "threshold_used": model.get_threshold(),
    }

    # Threshold sweep analysis
    if threshold_sweep:
        logger.info("\nPerforming threshold sweep analysis...")
        sweep_results = model.threshold_sweep(X_test, y_test)

        # Find optimal thresholds for different metrics
        optimal_f1 = model.find_optimal_threshold(X_test, y_test, metric="f1")
        optimal_precision = model.find_optimal_threshold(
            X_test, y_test, metric="precision"
        )
        optimal_recall = model.find_optimal_threshold(X_test, y_test, metric="recall")

        metrics["threshold_sweep"] = True
        metrics["optimal_threshold_f1"] = optimal_f1
        metrics["optimal_threshold_precision"] = optimal_precision
        metrics["optimal_threshold_recall"] = optimal_recall

        # Save sweep results
        results_dir = (
            Path(output_dir) if output_dir else Path("results/evaluate/gptclonebench")
        )
        results_dir.mkdir(parents=True, exist_ok=True)
        sweep_csv = results_dir / "threshold_sweep_results.csv"
        sweep_results.to_csv(sweep_csv, index=False)
        logger.info(f"Threshold sweep results saved to: {sweep_csv}")

        logger.info("\n" + "=" * 60)
        logger.info("THRESHOLD SWEEP ANALYSIS")
        logger.info("=" * 60)
        logger.info(f"Optimal threshold for F1: {optimal_f1:.3f}")
        logger.info(f"Optimal threshold for Precision: {optimal_precision:.3f}")
        logger.info(f"Optimal threshold for Recall: {optimal_recall:.3f}")
        logger.info(f"Current threshold: {model.get_threshold():.3f}")

        # Show best F1 from sweep
        best_f1_row = sweep_results.loc[sweep_results["f1"].idxmax()]
        logger.info(
            f"\nBest F1 from sweep: {best_f1_row['f1']:.4f} at threshold {best_f1_row['threshold']:.3f}"
        )

    # Print overall results
    logger.info("\n" + "=" * 60)
    logger.info("OVERALL RESULTS")
    logger.info("=" * 60)
    logger.info(f"Total pairs: {len(y_test)}")
    logger.info(
        f"Class distribution: {sum(y_test)} clones, {len(y_test) - sum(y_test)} non-clones"
    )
    logger.info("-" * 60)
    logger.info(f"Accuracy:  {metrics['accuracy']:.4f}")
    logger.info(f"Precision: {metrics['precision']:.4f}")
    logger.info(f"Recall:    {metrics['recall']:.4f}")
    logger.info(f"F1 Score:  {metrics['f1']:.4f}")
    logger.info(f"ROC AUC:   {metrics['roc_auc']:.4f}")
    logger.info("\nClassification Report:")
    logger.info(
        classification_report(y_test, y_pred, target_names=["Non-Clone", "Clone"])
    )

    # Analyze by prompt
    logger.info("\n" + "=" * 60)
    logger.info("ANALYSIS BY PROMPT")
    logger.info("=" * 60)
    prompt_analysis = analyze_by_prompt(y_test, y_pred, y_proba, metadata)
    if not prompt_analysis.empty:
        logger.info("\n" + prompt_analysis.to_string(index=False))

    # Analyze by clone type
    logger.info("\n" + "=" * 60)
    logger.info("ANALYSIS BY CLONE TYPE")
    logger.info("=" * 60)
    type_analysis = analyze_by_clone_type(y_test, y_pred, y_proba, metadata)
    if not type_analysis.empty:
        logger.info("\n" + type_analysis.to_string(index=False))

    # Save detailed results
    results_dir = (
        Path(output_dir) if output_dir else Path("results/evaluate/gptclonebench")
    )
    results_dir.mkdir(parents=True, exist_ok=True)

    # Save prompt analysis
    if not prompt_analysis.empty:
        prompt_csv = results_dir / "analysis_by_prompt.csv"
        prompt_analysis.to_csv(prompt_csv, index=False)
        logger.info(f"\nPrompt analysis saved to: {prompt_csv}")

    # Save type analysis
    if not type_analysis.empty:
        type_csv = results_dir / "analysis_by_clone_type.csv"
        type_analysis.to_csv(type_csv, index=False)
        logger.info(f"Clone type analysis saved to: {type_csv}")

    # Generate visualizations
    if visualize:
        logger.info("\nGenerating visualizations...")
        visualizer = MetricsVisualizer(output_dir=str(results_dir))

        feature_names = extractor.get_feature_names(fused=True)

        extra_info = {
            "dataset": "GPTCloneBench",
            "dataset_path": dataset_path,
            "language": language,
            "model_path": model_path,
            "total_samples": len(y_test),
            "feature_count": X_test.shape[1],
            "clone_types": str(type_analysis.to_dict())
            if not type_analysis.empty
            else "N/A",
        }

        report_files = visualizer.create_complete_report(
            y_true=y_test,
            y_pred=y_pred,
            y_scores=y_proba,
            metrics=metrics,
            feature_names=feature_names,
            importances=model.model.feature_importances_,
            extra_info=extra_info,
            report_name="gptclonebench_evaluation_report.html",
        )

        logger.info(f"Visualizations saved to: {report_files['html_report']}")
        metrics["visualization_path"] = str(report_files["html_report"])

    # Save metrics JSON
    import json as json_module

    metrics_json = results_dir / "metrics.json"
    with open(metrics_json, "w") as f:
        json_module.dump(metrics, f, indent=2)
    logger.info(f"Metrics saved to: {metrics_json}")

    return metrics


def main():
    """Main entry point."""
    parser = argparse.ArgumentParser(
        description="Evaluate Type-IV Clone Detector on GPTCloneBench"
    )

    parser.add_argument(
        "--model",
        type=str,
        required=True,
        help="Path to trained model (.pkl file)",
    )
    parser.add_argument(
        "--dataset",
        type=str,
        default="../../../../datasets/gptclonebench/gptclonebench_dataset.jsonl",
        help="Path to GPTCloneBench JSONL file",
    )
    parser.add_argument(
        "--language",
        type=str,
        default="java",
        choices=["java", "python", "c", "csharp"],
        help="Programming language",
    )
    parser.add_argument(
        "--sample-size",
        type=int,
        default=None,
        help="Sample size for evaluation (optional)",
    )
    parser.add_argument(
        "--clone-type-filter",
        type=str,
        default=None,
        choices=["T1", "T2", "T3", "T4"],
        help="Filter by clone type (e.g., T4 for Type-IV)",
    )
    parser.add_argument(
        "--visualize",
        action="store_true",
        default=True,
        help="Generate visualization reports",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help="Directory for output files (default: ./results/evaluate/gptclonebench)",
    )
    parser.add_argument(
        "--threshold",
        type=float,
        default=None,
        help="Custom decision threshold (default: use model's calibrated threshold)",
    )
    parser.add_argument(
        "--threshold-sweep",
        action="store_true",
        help="Perform threshold sweep analysis",
    )
    parser.add_argument(
        "--log-level",
        type=str,
        default="INFO",
        choices=["DEBUG", "INFO", "WARNING", "ERROR"],
        help="Logging level",
    )

    args = parser.parse_args()

    # Set logging level
    logging.getLogger().setLevel(getattr(logging, args.log_level))

    # Run evaluation
    metrics = evaluate_gptclonebench(
        model_path=args.model,
        dataset_path=args.dataset,
        language=args.language,
        sample_size=args.sample_size,
        clone_type_filter=args.clone_type_filter,
        visualize=args.visualize,
        output_dir=args.output_dir,
        threshold=args.threshold,
        threshold_sweep=args.threshold_sweep,
    )

    logger.info("\n" + "=" * 60)
    logger.info("EVALUATION COMPLETE")
    logger.info("=" * 60)
    for key, value in metrics.items():
        if isinstance(value, float):
            logger.info(f"{key}: {value:.4f}")
        else:
            logger.info(f"{key}: {value}")


if __name__ == "__main__":
    main()
